// Get converter by path
export const getConverterByPath = (categorySlug, converterSlug) => {
  const category = categories.find(c => c.slug === categorySlug);
  if (!category) return null;
  const converter = category.converters.find(c => c.slug === converterSlug);
  if (!converter) return null;
  return { converter, category };
};

